#include<iostream>
using namespace std;

int main()
{
    int a;
    cout<<"size of integer is: "<<sizeof(a)<<endl;
    long int b;
    cout<<"size of long integer is: "<<sizeof(b)<<endl;
    short int c;
    cout<<"size of short integer is: "<<sizeof(c)<<endl;
    float d;
    cout<<"size of float is: "<<sizeof(d)<<endl;
    double e;
    cout<<"size of double is: "<<sizeof(e)<<endl;

}